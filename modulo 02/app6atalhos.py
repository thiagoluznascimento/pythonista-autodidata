# 13 - Atalhos para programar 5x mais rápido no VS Code

# atalho para descomentar um codigo/linha: ctrl+k+u
# para comentar um bloco inteiro, usamos o shift+alt+a
# para descomentar um bloco inteiro, usamos o shift+alt+a
# para mover uma linha de codigo, usamos o alt+para baixo ou para cima
# para duplicar uma linha, usamos o alt+shift
# para exibir e esconder a barralateraldo vscod ctrl+b
# para abrir o terminal, usamos o ctrl+'  (acento grave)
# para pesquisar alguma coisa no codigo, usamos o ctrl+f . Esc para sair
# para REFATORAR usamos o ctrl+f, depois de selecionar o que quer refatorar, usamos o ctrl+h. Para modificar todos de uma vez, ctrl+alt+enter
# para alterar entre arquivos, ctrl+p . Se apertar ctrl+enter, a aba que vc está navegando abre ao lado. para alterar a aba, usamos o ctrl+1 ou ctrl+2. ctrl+w, fechar
# para acessar o codigo fonte da biblioteca que estamos usando, basta apertar f12 em cima dala


