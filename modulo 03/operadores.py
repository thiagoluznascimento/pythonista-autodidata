# Principais operadores no Python
#comparação 
idade = 15

print(idade > 17) #maior que
print(idade < 17) #menor que
print(idade >= 17) #maior ou igual
print(idade <= 17) #menor ou igual
print(idade == 17) #igual
print(idade != 17) #diferente

#comparação entre outros tipos
print(True == False)
print('Rafael' == 'rafael')
print('b' > 'a')
print(5 == '5')
print(type(5))
print(type('5'))


